public class Animal
{
    //Instance Variables
    public String type;
    public String sound;

    //Constructor
    public Animal(String type, String sound)
    {
        this.type = type;
        this.sound = sound;
    }

    //Getters
    public String getType()
    {
        return type;
    }

    public String getSound()
    {
        return sound;
    }
}